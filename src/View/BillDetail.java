    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            // Lấy STT hoặc index của dòng được chọn
            int stt = (int) jTable1.getValueAt(selectedRow, 0);
            // Xóa dữ liệu tương ứng từ ListDrink và ListQuantity
            if (stt >= 1 && stt <= CustomerRequest.drinks.size()) {
                CustomerRequest.drinks.remove(stt - 1);
                Orders.listQuantity.remove(stt - 1);
            }

            // Xóa dòng từ bảng
            ((DefaultTableModel) jTable1.getModel()).removeRow(selectedRow);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

